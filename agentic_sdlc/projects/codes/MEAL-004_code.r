## One-Pan Lemon Herb Roasted Chicken and Asparagus

**Yields:** 2 servings
**Prep time:** 10 minutes
**Cook time:** 20-25 minutes

**Ingredients:**
*   2 boneless, skinless chicken breasts (approx. 150g each)
*   250g asparagus, trimmed
*   1 lemon
*   2 tbsp olive oil
*   1 tsp dried oregano
*   1 tsp dried thyme
*   1/2 tsp garlic powder
*   Salt, to taste
*   Black pepper, to taste

**Equipment:**
*   1 large baking sheet
*   Parchment paper (optional, for easier cleanup)
*   Small bowl

**Instructions:**

1.  **Preheat Oven & Prep Baking Sheet:** Preheat your oven to 400°F (200°C). Line a large baking sheet with parchment paper for even easier cleanup (optional).

2.  **Prepare Chicken:** Pat the chicken breasts dry with a paper towel. Place them on one side of the prepared baking sheet.

3.  **Prepare Asparagus:** Wash and trim the tough ends off the asparagus. Arrange the asparagus on the other side of the baking sheet, next to the chicken.

4.  **Make Lemon Herb Mixture:** In a small bowl, whisk together the olive oil, juice of half the lemon, dried oregano, dried thyme, garlic powder, salt, and pepper. Reserve the other half of the lemon for serving.

5.  **Season Everything:** Drizzle about half of the lemon herb mixture over the chicken breasts and the remaining half over the asparagus. Ensure both are evenly coated.

6.  **Roast:** Place the baking sheet in the preheated oven. Roast for 20-25 minutes, or until the chicken is cooked through (internal temperature reaches 165°F or 74°C) and the asparagus is tender-crisp.

7.  **Serve:** Remove the baking sheet from the oven. Slice the chicken breasts and serve immediately with the roasted asparagus. Squeeze the reserved lemon half over the dish before serving, if desired.

**RAW INGREDIENT LIST (FOR MEAL KIT PRE-PORTIONING):**
- Boneless, skinless chicken breasts: 300g
- Asparagus, trimmed: 250g
- Lemon: 1 whole
- Olive oil: 30ml (2 tbsp)
- Dried oregano: 5g (1 tsp)
- Dried thyme: 5g (1 tsp)
- Garlic powder: 2.5g (1/2 tsp)
- Salt: To taste (approx. 3g)
- Black pepper: To taste (approx. 1g)