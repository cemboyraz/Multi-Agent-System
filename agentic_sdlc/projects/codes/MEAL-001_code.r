## Lemon Herb Sheet Pan Chicken and Asparagus

**Prep time:** 10 minutes
**Cook time:** 20 minutes
**Total time:** 30 minutes

This recipe delivers a flavorful and healthy meal with minimal fuss, perfect for a quick weeknight dinner.

**Yields:** 1 serving

**Ingredients:**

*   1 (6 oz) boneless, skinless chicken breast
*   1 cup asparagus spears, trimmed
*   1/2 cup cherry tomatoes
*   1 tablespoon olive oil
*   1/2 teaspoon dried oregano
*   1/2 teaspoon dried thyme
*   1/4 teaspoon garlic powder
*   Salt, to taste
*   Black pepper, to taste
*   1/4 lemon, cut into wedges

**Instructions:**

1.  **Preheat Oven:** Preheat your oven to 400°F (200°C).
2.  **Prepare Chicken:** Pat the chicken breast dry with paper towels. In a small bowl, mix together 1/2 tablespoon of olive oil, oregano, thyme, garlic powder, salt, and pepper. Rub this mixture all over the chicken breast.
3.  **Prepare Vegetables:** In a medium bowl, toss the trimmed asparagus and cherry tomatoes with the remaining 1/2 tablespoon of olive oil, salt, and pepper.
4.  **Assemble Sheet Pan:** Place the seasoned chicken breast in the center of a baking sheet. Arrange the seasoned asparagus and cherry tomatoes around the chicken.
5.  **Bake:** Bake for 20-25 minutes, or until the chicken is cooked through (internal temperature reaches 165°F or 74°C) and the asparagus is tender-crisp. The cherry tomatoes should be slightly burst and softened.
6.  **Serve:** Remove from oven. Serve the chicken breast with the roasted vegetables. Squeeze fresh lemon juice from the lemon wedges over the entire dish before serving.

