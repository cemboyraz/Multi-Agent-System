## Speedy Tofu & Black Bean Fiesta

**Prep time:** 10 minutes
**Cook time:** 15 minutes
**Total time:** 25 minutes

**Yields:** 1 serving

**Ingredients:**
*   1 block (350g) extra-firm tofu, pressed and cubed
*   1 tbsp olive oil
*   1 tsp chili powder
*   1/2 tsp cumin
*   1/4 tsp garlic powder
*   1/4 tsp salt
*   Pinch of black pepper
*   1/2 cup canned black beans, rinsed and drained
*   1/4 cup corn kernels (fresh or frozen, thawed)
*   1/4 cup diced red bell pepper
*   2 tbsp diced red onion
*   1 tbsp chopped fresh cilantro
*   1 tbsp lime juice
*   Salt and pepper to taste

**Instructions:**
1.  **Prep Tofu:** Pat the pressed tofu cubes dry with a paper towel. In a medium bowl, toss tofu with olive oil, chili powder, cumin, garlic powder, salt, and pepper until evenly coated.
2.  **Sear Tofu:** Heat a non-stick skillet over medium-high heat. Add the seasoned tofu cubes in a single layer. Cook for 5-7 minutes per side, until golden brown and crispy.
3.  **Make Salsa:** While tofu is searing, combine rinsed black beans, corn, diced red bell pepper, diced red onion, chopped cilantro, and lime juice in a small bowl. Season with salt and pepper to taste.
4.  **Serve:** Divide the seared tofu onto a plate. Top generously with the black bean and corn salsa.

